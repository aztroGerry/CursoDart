void main() {
  /// uso de simbolos de comparacion en Dart
  /*          autor: gerardo garduño 
              fecha: 16 de diciembre      */
  // uso de los operadores de comparación que tendra como salida un booleano

  int edad = 18;

  // mayor 
  // si edad es mayor que 20 
  print(edad>20); // false

  // menor 
  // si edad es menor que 20 
  print(edad<20); // true 

  // mayor o igual 
  // si edad es mayor o igual que 20
  print(edad>=20); // false

  // menor o igual 
  // si edad es menor o igual que 20 
  print(edad<=20); // true 

  // igual
  // si edad es igual a 20
  print(edad==20); // false 

  // distinto 
  // si edad es distinto que 20
  print(edad!=20); // true
}
