void main() {
  /// uso del ciclo do while en Dart
  /*             autor: gerardo gardu√±o 
                 fecha: 16 de diciembre           */

  // do while se espera que entre una vez si esta condicion se cumple 
  int edad = 35;

  // se sale por que la edad ya se cumplio
  do
  {
    print('la edad es: ${edad}');
    ++edad;
  }while(edad <= 17);
  
}
