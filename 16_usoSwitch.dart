void main() {
   /// uso de la condicion en Dart
   /*             autor: gerardo garduño 
                 fecha: 16 de diciembre           */

  // el switch permite entrar a una variable que sera tomada dentro de la ejeccion del código
   int cal = 7;

  switch (cal)
  {
    case 10:
      print('excelente');
      break;
    case 9:
      print('muy bien');
      break;
    case 8:
      print('bien');
      break;
    case 7:
      print('haz pasado');
      break;
    default:
      print('reprobado');
      
  }
}
