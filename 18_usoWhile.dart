void main() {
  /// uso del ciclo while en Dart
  /*            autor: gerardo garduño 
                fecha: 16 de diciembre           */

  // while se va a repetir hasta que la condición se cumpla 
  int edad = 0;
  
  // cuando ya no se cumpla se va a salir del código 
  while(edad <= 18)
  {
    print('la edad es ${edad}');
    ++edad;
  }
  
}
