void main() {
  /// aplicacion del incremento y decremento en variables Dart
  /*               autor: gerardo gardu√±o 
                   fecha: 16 de diciembre             */

  // incremento y decremento     
   int numero = 12;
  int numero1 = 10;
   
  // preincremento 
  /// se incrementa y despues se usa la variabld
  ++numero;
  print(numero); // 13

  // predecremento
  --numero1;
  print(numero1);

  // post incremento
  /// se usa con el valor que tiene y al final se queda con otro valor
  numero++;
  print(numero);

  numero1--;
  print(numero1);
}
